export function getTeamColors(teamName) {
  const key = teamName?.toLowerCase?.().replace(/\s+/g, '') || '';
  return (
    TEAM_COLOR_MAP[key] || {
      primary: '#CCCCCC',
      secondary: '#888888',
      tertiary: '#444444',
      text: '#FFFFFF',
      bg: '#222222',
      classSet: {
        bg: 'bg-gray-700',
        text: 'text-white',
        border: 'border-white',
      },
    }
  );
}

export function getTeamClassSet(teamName) {
  return getTeamColors(teamName).classSet;
}
